import Foundation

// #9 SW Architecture와 Design Pattern 그리고 MVC패턴

/*
 SW Architecture는 건물의 아키텍쳐와 유사하다.
 건물을 지을때 아파트를 지을건지, 한옥을 지을건지, 주택을 지을건지 다양한 방법이 있다.
 소프트웨어 아키텍처도 유사하게 활용 용도에 따라 사용할 컴퓨터 언어를 선택해야하고
 또한 이것을 변경(건축물을허물고 다시지음) 하려고하면 비용이 많이 들것이다.
 
 디자인 패턴은 공사 기법이라고 할수도있다.
 건물을 지을때 최적화된 공사 기법을 이용해 안정성을 높이고 위험을 방지할수도있다.
 이는 디자인 패턴에서도 적용이 된다. 질높은 유지보수성과 재사용성, 안정성, 요구사항 반영 등등 많은 이점이 있다.
 
 MVC패턴은 역할에 따라 View, Controller, Model 이렇게 나뉜다.
 View를 통해 사용자에게 내용물을 표시해주고 입력받은 정보를 Controller에게 전달한다.
 Controller가 받은정보를 가공하여 Model에게 전달한다.
 Model은 가공된 데이터를 저장한다.
 
 이렇게 서로 하는 역할이 달라 M,V,C로 나눈것같다.
 */
